
#!/bin/bash
declare -A m
IFS=',' read -ra data < input.txt
for i in "${!data[@]}"; do m[$i]=${data[i]}; done
p=0 r=0 out=0
while :; do
    c=${m[$p]}
    o=$((c % 100))
    [[ $o -eq 99 ]] && break
    m1=$((c/100%10)); v1=${m[$((p+1))]:-0}; [[ $m1 -eq 0 ]] && a1=$v1 || { [[ $m1 -eq 1 ]] && a1=$((p+1)) || a1=$((r+v1)); }
    m2=$((c/1000%10)); v2=${m[$((p+2))]:-0}; [[ $m2 -eq 0 ]] && a2=$v2 || { [[ $m2 -eq 1 ]] && a2=$((p+2)) || a2=$((r+v2)); }
    m3=$((c/10000%10)); v3=${m[$((p+3))]:-0}; [[ $m3 -eq 0 ]] && a3=$v3 || { [[ $m3 -eq 1 ]] && a3=$((p+3)) || a3=$((r+v3)); }
    case $o in
        1) m[$a3]=$(( ${m[$a1]:-0} + ${m[$a2]:-0} )); p=$((p+4)) ;;
        2) m[$a3]=$(( ${m[$a1]:-0} * ${m[$a2]:-0} )); p=$((p+4)) ;;
        3) m[$a1]=2; p=$((p+2)) ;;
        4) out=${m[$a1]:-0}; p=$((p+2)) ;;
        5) (( ${m[$a1]:-0} != 0 )) && p=${m[$a2]:-0} || p=$((p+3)) ;;
        6) (( ${m[$a1]:-0} == 0 )) && p=${m[$a2]:-0} || p=$((p+3)) ;;
        7) (( ${m[$a1]:-0} < ${m[$a2]:-0} )) && m[$a3]=1 || m[$a3]=0; p=$((p+4)) ;;
        8) (( ${m[$a1]:-0} == ${m[$a2]:-0} )) && m[$a3]=1 || m[$a3]=0; p=$((p+4)) ;;
        9) r=$((r + ${m[$a1]:-0})); p=$((p+2)) ;;
    esac
done
echo $out
